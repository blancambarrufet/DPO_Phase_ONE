package presentation;

/**
 * Enumeration representing the menu options available for team management.
 * Each option corresponds to a specific action that can be performed on teams.
 */
public enum TeamManagementMenu {

    /**
     * Option to create a new team.
     */
    CREATE_TEAM,

    /**
     * Option to list all existing teams.
     */
    LIST_TEAM,

    /**
     * Option to delete a team from the system.
     */
    DELETE_TEAM,

    /**
     * Option to return to the previous menu.
     */
    BACK
}
