package presentation;

/**
 * Enumeration representing the main menu options available in the application.
 * Each option corresponds to a specific feature that the user can select.
 */
public enum MainMenu {

    /**
     * Option to list all available characters.
     */
    LIST_CHARACTERS,

    /**
     * Option to manage teams, including creation, listing, and deletion.
     */
    MANAGE_TEAMS,

    /**
     * Option to list all available items in the system.
     */
    LIST_ITEMS,

    /**
     * Option to simulate a combat match between teams.
     */
    SIMULATE_COMBAT,

    /**
     * Option to exit the application.
     */
    EXIT
}
